	public List<Director> getRelazioneDirettoreAttore(Map<Integer, Director> idMap, Actor a, Integer anno) {
		String sql= "SELECT md.director_id, r.actor_id "
				+ "FROM movies_directors md, movies m, roles r "
				+ "WHERE md.movie_id=m.id AND m.id=r.movie_id AND r.actor_id=? AND m.year=?"; 
		
		List<Director> result = new ArrayList<Director>();
		Connection conn = DBConnect.getConnection();

		try {
			PreparedStatement st = conn.prepareStatement(sql);
			st.setInt(1, a.getId());
			st.setInt(2, anno);
			ResultSet res = st.executeQuery();
			while (res.next()) {
				result.add(idMap.get(res.getInt("director_id")));
				}
				
			conn.close();
			return result; 
			
		} catch (SQLException e) {
			e.printStackTrace();
			System.out.println("Errore connessione al database");
			throw new RuntimeException("Error Connection Database");
		}
		
	}
	
	
	
